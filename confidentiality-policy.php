<h1>Politica de confidențialitate</h1>

<p>Acest document explică cum utilizăm datele personale colectate prin intermediul site-ului zenhomecleaning.ro.</p>
<p>Vom folosi datele dumneavoastră personale doar în scopuri comerciale și pentru a putea furniza serviciile oferite de către zenhomecleaning.ro.</p>


<h2>Ce date colectăm de la dumneavoastră?</h2>
<p>Colectăm următoarele date personale despre dumneavoastră, atunci când alegeți să utilizați formularul de contact pentru a ne trimite un mesaj: nume, email și numărul de telefon.</p>
<p>Orice acord/consimțământ dat se va înregistra în baza de date în scopul confirmarii acordului/consimțământului.</p>

<h2>Ce facem cu datele personale colectate?</h2>
<p>Numele, adresa de email și numărul de telefon vor fi stocate într-un mediu securizat (bază de date) și pot fi păstrate timp de 10 ani pentru motive contabile, conform legislației din România.</p>
<p>Dupa expirarea termenului de păstrare, vom proceda la stergerea acestora.</p>

<h2>Partajarea și transferul datelor</h2>
<p>Salvăm doar datele necesare furnizării serviciilor oferite de către noi și serviciilor oferite prin terți.</p>
<p>Toate acordurile/consimțământurile vor fi salvate pe un mediu securizat de stocare (bază de date pe un hard disk cu acces securizat prin username și parolă).</p>
<p>Informațiile cu caracter personal pot fi furnizate și către Parchetul General, Poliție, instanțele judecătorești și altor organe abilitate ale statului, în baza și în limitele prevederilor legale și ca urmare a unor cereri expres formulate.</p>

<h2>Marketing si campanii publicitare</h2>
<p>Nu vom folosi datele furnizate de dumneavoastră pentru marketing și campanii online decât cu acordul dumneavoastră expres și neechivoc.</p>
<p>Dacă optați pentru a primi email-uri informative legate de promoțiile noastre, produse cu preț redus și campanii publicitare, aveți dreptul de a vă retrage acordul oricând printr-un email la office@zenhomecleaning.ro.</p>
<p>Toate acordurile pentru marketing și campanii publicitare vor fi salvate pe un mediu securizat de stocare (bază de date pe un hard disk cu acces securizat prin username și parola).</p>

<h2>Acces la informatie, corectare si dreptul de a fi uitat</h2>
<p>Vrem să ne asigurăm că datele pe care le deținem sunt corecte și de actualitate, de aceea vom trece la ștergerea/modificarea datelor invalide sau incorecte.</p>
<p>Accesul la informațiile salvate de noi se poate cere prin email la office@zenhomecleaning.ro sau folosind formularul de contact.</p>
<p>Editarea informațiilor salvate de noi se poate solicita prin email la office@zenhomecleaning.ro sau folosind formularul de contact.</p>
<p>Dacă dorești să ștergi contul de client sau datele personale, aceasta se poate face printr-o cerere scrisă prin email la office@zenhomecleaning.ro sau prin cerere verbală prin telefon la +40720997887.</p>

<h2>Securitate</h2>
<p>Ne asigurăm că tot conținutul de pe site este sigur, fără cod malițios și fără breșe de securitate. Zenhomecleaning.ro se obligă ca la orice fel de breșă de securitate privind datele cu caracter personal, toți utilizatorii ale căror date au fost dezvăluite/furate vor fi anunțați.</p>
<p>Daca apar suspiciuni asupra unor informații primite de dvs. prin email sau prin telefon, vă rugăm să ne contactați.</p>

<h2>Ce poți să faci ca să te protejezi?</h2>
<p>- Întotdeauna să folosești parole lungi și greu de ghicit, care să nu conțină numele tău, adresa de email sau cuvântul ‘parola’.</p>
<p>- Asigură-te că parola conține numere, cifre și alte caractere speciale.</p>
<p>- Nu spune parola nimănui, inclusiv nouă. Zen Home Cleaning nu îți va cere niciodată parola.</p>
<p>- Schimbă-ți parola cu una nouă cel puțin o dată la 3 luni.<p>
<p>- Încearcă să îți memorezi parola, să nu fie nevoie să o notezi undeva.</p>
<p>- Folosește parole diferite pentru fiecare cont de pe orice website.</p>

<h2>Securitatea conexiunilor</h2>

<p>Website-ul zenhomecleaning.ro folosește conexiuni securizate prin SSL/TLS (Transport Layer Security), la standardele din industrie, pentru a procesa datele pe care ni le transimiteți.</p>

